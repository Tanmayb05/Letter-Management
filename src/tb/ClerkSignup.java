    private void SignupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignupButtonActionPerformed
        // TODO add your handling code here:
        try{
            
            if(isValidPassword(PasswordField.getText()) != true) {
                JOptionPane.showMessageDialog(null, "Invalid Password");
            }
            else {
                ConnectionEstablish con = new ConnectionEstablish();
                String sql = "INSERT INTO clerk VALUES (?, ?, ?, ?, ?)";
                PreparedStatement st = con.c.prepareStatement(sql);
                
                st.setString(1, IDField.getText());
                st.setString(2, NameField.getText());
                st.setString(3, EmailField.getText());
                st.setString(4, UsernameField.getText());
                st.setString(5, PasswordField.getText()); 
                
                
                int i = st.executeUpdate();
                if (i>0) {
                    JOptionPane.showMessageDialog(null, "Succesfully Created Profile");
                    this.setVisible(false);
                    new ClerkLogin().setVisible(true);
                }
                
                clearInputs();
            }
        } catch(HeadlessException | SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_SignupButtonActionPerformed

